#region add function 

int add(int x, int y)
{
    return x + y;
}

#endregion

#region sub function
int sub(int x, int y)
{
    return x - y;
}

#endregion

#region mul function
int mul(int x, int y)
{
    return x * y;
}

#endregion

#region div function

int div(int x, int y)
{
    return x / y;
}

#endregion